#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*k;

	i = 0;
	k = b;
	while (i < len)
	{
		k[i] = c;
		i++;
	}
	return (b);
}
