#include "libft.h"

int	ft_atoi(char *str)
{
	int k;
	int i;
	int r;

	k = 0;
	r = 1;
	i = 0;
	while (((str[i] != '\0') && ((str[i] == '\f') || (str[i] == '\t') || \
	(str[i] == '\n') || (str[i] == '\v') || (str[i] == '\r') || \
	(str[i] == ' '))))
		i++;
	if (str[i] == '-')
		r = r * (-1);
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while ((str[i] != '\0') && (str[i] >= 48) && (str[i] <= 57))
	{
		k = k * 10 + (str[i] - '0');
		i++;
	}
	return (r * k);
}
